// Declaración de variables con var, let y const
var nombre = "Juan"; // No recomendado en la mayoría de los casos
let edad = 25;
const PI = 3.1416;

console.log("Nombre:", nombre);
console.log("Edad:", edad);
console.log("Valor de PI:", PI);

// Operadores de Asignación
let numero = 10;
numero = 20; // Se reasigna el valor
console.log("Número reasignado:", numero);

// Operadores Matemáticos
let suma = 5 + 3;
let resta = 10 - 2;
let multiplicacion = 4 * 3;
let division = 20 / 4;

console.log("Suma:", suma);
console.log("Resta:", resta);
console.log("Multiplicación:", multiplicacion);
console.log("División:", division);

// Operadores Combinados
let contador = 5;
contador += 2; // Equivalente a contador = contador + 2
console.log("Contador después de += 2:", contador);

contador *= 3; // Equivalente a contador = contador * 3
console.log("Contador después de *= 3:", contador);
